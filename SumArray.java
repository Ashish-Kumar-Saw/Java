package collegeWallah.Array;

public class SumArray {
    public static void main(String[] args) {
        int[] Array = {1, 3, 54, 2, 523, 5};
        int sum = 0;
        for (int i = 0;i<Array.length;i++) {
            sum = sum + Array[i];
        }
        System.out.println(sum);
        }
    }


